

//PERGUNTA1

// Obtenha uma referência ao elemento canvas
var ctx = document.getElementById('myChart').getContext('2d');
var excelente1 = document.getElementById('excelente1').value;
var bom1 = document.getElementById('bom1').value; 
var regular1 = document.getElementById('regular1').value; 
var ruim1 = document.getElementById('ruim1').value;   
// Defina os dados do gráfico de pizza
var data = {
  labels: ['Excelente', 'Bom', 'Regular','Ruim',],
  datasets: [{
    data: [excelente1, bom1, regular1, ruim1], // Valores correspondentes aos rótulos
    backgroundColor: ['#27AE60', '#3498DB', '#F1C40F','#C0392B'] // Cores para cada fatia
  }]
};

// Crie o gráfico de pizza
var myPieChart = new Chart(ctx, {
  type: 'pie',
  data: data
});



